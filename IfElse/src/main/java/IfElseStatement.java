public class IfElseStatement {
    public static void main(String[] args) {
        int a = 5, b = 5;
        if(a >= 5){
            System.out.println("A >= 5");
        }else {
            System.out.println("A < 5");
        }

        if(b >= 6){
            System.out.println("B >= 6");
        }else {
            System.out.println("B < 6");
        }
    }
}
