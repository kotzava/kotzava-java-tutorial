public class IfStatement {
    public static void main(String[] args) {
        int a = 5;
        if (a > 5){
            System.out.println("A > 5");
        }
        if (a < 5){
            System.out.println("A < 5");
        }
        if (a == 5){
            System.out.println("A = 5");
        }
    }
}
